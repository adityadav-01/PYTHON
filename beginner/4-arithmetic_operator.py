a ,b = 10,20 

print(a + b)  # Addition: 10 + 20 = 30
print(a - b)  # Subtraction: 10 - 20 = -10
print(a * b)  # Multiplication: 10 * 20 = 200
print(a / b)  # Division: 10 divided by 20 = 0.5
print(a % 3)  # Modulus: Remainder of 10 divided by 3 = 1
print(5 ** 3)  # Exponentiation: 5 to the power of 3 = 125
print(2 ** 4)  # Exponentiation: 2 to the power of 4 = 16
print(10 / 3)  # Division: 10 divided by 3 = 3.333...
print(round(10 / 3, 1))  # Rounding: 10 divided by 3 rounded to 1 decimal = 3.3
print(10 // 3)  # Floor division: 10 divided by 3 without decimals = 3